    private void jButton1VerOcultartablaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1VerOcultartablaActionPerformed
        // Cambia la visibilidad de la tabla
        if (isTableVisible) {
            jScrollPane1.setVisible(false);
            jButton1VerOcultartabla.setText("Ver tabla");
        } else {
            jScrollPane1.setVisible(true);
            jButton1VerOcultartabla.setText("Ocultar tabla");
        }
        isTableVisible = !isTableVisible;
        revalidate();
        repaint();
    }//GEN-LAST:event_jButton1VerOcultartablaActionPerformed

