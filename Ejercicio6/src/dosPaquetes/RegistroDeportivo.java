    public void desplazarVistaTabla() {
        int lastRow = modeloTabla.getRowCount() - 1; // Índice de la última fila
        if (lastRow >= 0) {
            jTable1.scrollRectToVisible(jTable1.getCellRect(lastRow, 0, true));
        }
    }

